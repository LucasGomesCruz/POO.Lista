package Lista5.Aula6;

public class ContaBancaria {
    public String agencia;
    public String numero;
    public Double saldo;

    public ContaBancaria()
    {   
    }

    public ContaBancaria(String agencia, String numero, Double saldo)
    {
        this.agencia = agencia;
        this.numero = numero;
        this.saldo = saldo;
    }
}
